// Function to get initials for fallback
export const getSubscriptionInitials = (subscriptionName: string): string => {
  if (!subscriptionName) return '?';
  
  const words = subscriptionName.split(' ');
  if (words.length === 1) {
    return subscriptionName.substring(0, 2).toUpperCase();
  }
  
  return words.map(word => word[0]).join('').toUpperCase().substring(0, 2);
};

